package com.pod2.elevator.main;

public class SimulationAlreadyRunningException extends Exception {

	public SimulationAlreadyRunningException() {
	}

	public SimulationAlreadyRunningException(String arg0) {
		super(arg0);
	}

	public SimulationAlreadyRunningException(Throwable arg0) {
		super(arg0);
	}

	public SimulationAlreadyRunningException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
