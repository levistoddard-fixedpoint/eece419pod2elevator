package com.pod2.elevator.main;

public class UnableToStopSimulationException extends Exception {

	public UnableToStopSimulationException() {
	}

	public UnableToStopSimulationException(String arg0) {
		super(arg0);
	}

	public UnableToStopSimulationException(Throwable arg0) {
		super(arg0);
	}

	public UnableToStopSimulationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
